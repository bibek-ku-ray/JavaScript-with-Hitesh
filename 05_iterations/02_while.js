//while and do-while

let index = 0
while (index <= 10) {
    console.log(`value: ${index}`);
    index += 2
}

let arr = 0
let superHero = ["batman", "flash", "superman","spiderman"]
while (arr < superHero.length) {
    console.log(`Value: ${superHero[arr]}`);
    arr++
}



let score = 12
do {
    console.log(`score: ${score}`);
    score++
} while (score <= 10);